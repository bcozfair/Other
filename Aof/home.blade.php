                    1997 specializes in the IC assembly and test of low to
                    medium pin count IC's. We offer state of the art
                    assembly processes (including Copper Wire, Chip on Lead,
                    Multi-Die, Stacked Die, Clear Molding, System In Package
                    etc.) and test solutions. Our "leadless" package size
                    starts from a 2 pin 0.6 x 0.3 mm to our popular package
                    size of 40 pin 12x12 mm. Our leaded packages starts from
                    our SOT963 with foot print of 1x1 mm to standard 24L
                    SOIC.
